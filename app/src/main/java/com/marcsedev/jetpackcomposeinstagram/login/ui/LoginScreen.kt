@Composable
fun LoginScreen(loginViewModel: LoginViewModel/*navController: NavController*/) {
    Box(
        Modifier
            .fillMaxSize()
            .padding(16.dp)
            .background(Color.Transparent),  // Set background color to white
    ) {
        val isLoading: Boolean by loginViewModel.isLoading.observeAsState(false)
        if (isLoading) {
            Box(
                modifier = Modifier
                    .fillMaxSize()
            ) {
                CircularProgressIndicator(
                    Modifier
                        .align(Alignment.Center)
                        .animateContentSize()
                )
            }
        } else {
            Header(Modifier.align(TopEnd))
            Body(Modifier.align(Center), loginViewModel)
            Footer(Modifier.align(BottomCenter))
        }
    }
}

@Composable
fun Header(modifier: Modifier) {
    val context = LocalContext.current

    Icon(
        imageVector = Icons.Default.Close,
        contentDescription = "close app",
        modifier = modifier.clickable {
            (context as? Activity)?.finish() // Check and cast to Activity
        }
    )
}


@Composable
fun Body(modifier: Modifier, loginViewModel: LoginViewModel) {
    val email by loginViewModel.email.observeAsState("")
    val password by loginViewModel.password.observeAsState("")
    val isLoginEnable by loginViewModel.isLoginEnable.observeAsState(false)


    Column(modifier = modifier) {
        ImageLogo(modifier = Modifier.align(CenterHorizontally))
        Spacer(modifier = Modifier.size(16.dp))
        Email(email) {
            loginViewModel.onLoginChanged(it, password)
        }
        Spacer(modifier = Modifier.size(4.dp))
        Password(password) {
            loginViewModel.onLoginChanged(email, it)
        }
        Spacer(modifier = Modifier.size(8.dp))
        ForgotPassword(Modifier.align(Alignment.End))
        Spacer(modifier = Modifier.size(16.dp))
        LoginButton(isLoginEnable, loginViewModel)
        Spacer(modifier = Modifier.size(16.dp))
        LoginDivider()
        Spacer(modifier = Modifier.size(32.dp))
        SocialLogin()


    }
}

